/** Get places by category */
export function getPlacesByCategory(category: string): Place[] {
  return places.filter((p) => p.category === category);
}

/** Get places by region */
export function getPlacesByRegion(region: string): Place[] {
  return places.filter((p) => p.region === region);
}

/** Get alternative to a crowded place */
export function getAlternativeFor(placeId: string): Place[] {
  const place = places.find((p) => p.id === placeId);
  if (!place) return [];

  const alternatives: Record<string, string[]> = {
    seongsan: ['udo', 'jimibong'],
    yosmea: ['hwanguji', 'songaksan'],
    saryani: ['bijarim', 'dongbaek'],
    hyeopjae: ['geumneung', 'gwakji'],
    hallasan: ['wiseooreum', 'saebyeol'],
  };

  const altIds = alternatives[placeId] || [];
  return altIds.map((id) => places.find((p) => p.id === id)).filter(Boolean) as Place[];
}

/** Season-based recommendations */
export function getSeasonalRecommendations(month: number): Place[] {
  if (month >= 6 && month <= 8) {
    // Summer: coastal, waterfalls
    return places.filter((p) =>
      p.tags.some((t) => ['해수욕장', '해안', '폭포', '여름'].includes(t))
    );
  } else if (month >= 12 || month <= 2) {
    // Winter: camellia, highland, christmas
    return places.filter((p) =>
      p.tags.some((t) => ['겨울', '동백', '고지대', '크리스마스'].includes(t))
    );
  }
  // Spring/Fall: forests, trails
  return places.filter((p) =>
    p.tags.some((t) => ['숲', '힐링', '트레킹', '수목원'].includes(t))
  );
}
